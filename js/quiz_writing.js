let resultsQuiz1 = {};

const DATA_id_question = document.getElementById('quiz_1-id_question');
const DATA_question = document.getElementById('quiz_1-question');
const DATA_quiz_answers = [
    document.getElementById('quiz_1-answers_1'), document.getElementById('quiz_1-answers_2'),
    document.getElementById('quiz_1-answers_3'), document.getElementById('quiz_1-answers_4')
];
const sectionQuiz1 = document.getElementById('section-quiz_1');
const quiz1 = document.getElementById('quiz_1');
const quizHeading = document.getElementById('quiz_1-heading');
const quizText = document.getElementById('quiz_1-text');
const questionsQuiz1 = document.getElementById('questions-quiz_1');
const btnNextQuiz1 = document.getElementById('quiz_1-btn-next');
const progressLineQuiz1 = document.getElementById('quiz_1-red-Line');
const quizResult = document.getElementById('quiz_1-result');
const quizOutputResult = document.getElementById('quiz_1-output-result');
const quizRestart = document.getElementById('quiz_1-btn-result-restart');

const renderQuestionsQuiz1 = (index) => {
    renderIndicatorQuiz1(index);
    questionsQuiz1.dataset.currentStep = index;

    DATA_id_question.innerHTML = `${DATA_quiz1[index].id_question}`;
    DATA_question.innerHTML = `<b>${DATA_quiz1[index].question}</b>`;
    for(let i=0; i<DATA_quiz1[index].answers.length; i++) {
        DATA_quiz_answers[i].innerHTML = `
            <input name="${index}" type="radio" id="quiz_1-radio_${+ DATA_quiz1[index].answers[i].id}" value="${DATA_quiz1[index].answers[i].id}" class="answer-input-quiz_1">
            <label for="quiz_1-radio_${+ DATA_quiz1[index].answers[i].id}">${DATA_quiz1[index].answers[i].value}</label>
        `;
    }
};

const renderResultQuiz1 = () => {
    let sumAnswer = 0;

    for(let i = 0; i < DATA_quiz1.length; i++) {
        for(let j = 0; j < DATA_quiz1[i].answers.length; j++) {
            if (DATA_quiz1[i].answers[j].correct && DATA_quiz1[i].answers[j].id == resultsQuiz1[i]) {
                sumAnswer++;
            }
        }
    }
    quiz1.style.visibility = 'hidden';
    quizHeading.innerHTML = "ваш результат";
    quizText.innerHTML = "Правильних відповідей";
    quizResult.style.visibility = 'visible';
    quizOutputResult.innerHTML = `${sumAnswer}/${DATA_quiz1.length}`;
};

const renderIndicatorQuiz1 = (index) => {
    progressLineQuiz1.style.width = `${(index + 1)*(100/DATA_quiz1.length)}%`;
};

const restartQuiz1 = () => {
    quiz1.style.visibility = 'visible';
    quizResult.style.visibility = 'hidden';
    renderQuestionsQuiz1(0);
};

questionsQuiz1.addEventListener('change', (event) => {
    if(event.target.classList.contains('answer-input-quiz_1')) {
        resultsQuiz1[event.target.name] = event.target.value;

        btnNextQuiz1.disabled = false;
    }
});

btnNextQuiz1.onclick = function() {
    const nextQuestionIndex = Number(questionsQuiz1.dataset.currentStep) + 1;
    if(DATA_quiz1.length == nextQuestionIndex) {
        renderResultQuiz1(); //Переход к результатам
    } else {
        renderQuestionsQuiz1(nextQuestionIndex); //Переход к след вопросу
    }
    btnNextQuiz1.disabled = true;
}

quizRestart.onclick = function() {
    resultsQuiz1 = {};
    restartQuiz1();
    renderQuestionsQuiz1(0);
}
renderQuestionsQuiz1(0);